#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int next_permutation(int n, char **s)
{
    int i = n - 2;
    while (i >= 0 && strcmp(s[i], s[i + 1]) >= 0) i--;
    if (i < 0) return 0;
    int j = n - 1;
    while (strcmp(s[i], s[j]) >= 0) j--;
    char *t = s[i]; s[i] = s[j]; s[j] = t;
    int l = i + 1, r = n - 1;
    while (l < r) { t = s[l]; s[l] = s[r]; s[r] = t; l++; r--; }
    return 1;
}